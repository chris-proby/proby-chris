def update_md():
    md_path = BASE_PATH + "가이드라인_2차_질문별_응답정리_4명.md"
    with open(md_path, 'r', encoding='utf-8') as f:
        content = f.read()

    lines = content.split('\n')
    new_lines = []

    for line in lines:
        # 메타데이터 응답 원본 줄 업데이트
        if '`4 - 알 수 없는 그룹.txt` (김영균)' in line and '응답 원본' in line:
            line = line.replace(
                '`4 - 알 수 없는 그룹.txt` (김영균)',
                '`4 - 알 수 없는 그룹.txt` (김영균), `5 - 알 수 없는 그룹.txt` (김남호), `6 - 알 수 없는 그룹.txt` (이민슷), `7 - 알 수 없는 그룹.txt` (조수정)'
            )
        # 테이블 헤더 줄 (참여자 열 포함)
        elif '참여자 A (전수현)' in line and '| ID |' in line:
            line = line.rstrip()
            if not line.endswith('|'):
                line += ' |'
            # 기존 마지막 | 제거 후 새 열 추가 (| 사이에 공백 유지)
            line = line[:-1] + '| 참여자 E (김남호) | 참여자 F (이민슷) | 참여자 G (조수정) |'
        # 구분선 줄 (테이블 헤더 다음 줄)
        elif re.match(r'^\|----', line) and '참여자' not in line and '항목' not in line and '표기' not in line and 'ID' not in line:
            line = line.rstrip()
            if not line.endswith('|'):
                line += '|'
            line = line + '-------------------|-------------------|-------------------|'
        # 데이터 행 (Q번호로 시작)
        elif re.match(r'^\| \*\*Q', line):
            q_match = re.search(r'\*\*Q(\d+(?:-\d+)?)\*\*', line)
            if q_match:
                q_id = "Q" + q_match.group(1)
                if q_id in NEW_DATA:
                    e_cell = NEW_DATA[q_id].get("E", NA)
                    f_cell = NEW_DATA[q_id].get("F", NA)
                    g_cell = NEW_DATA[q_id].get("G", NA)
                    line = line.rstrip()
                    if line.endswith('|'):
                        line = line[:-1].rstrip()
                    line += f' | {e_cell} | {f_cell} | {g_cell} |'

        new_lines.append(line)

    updated = '\n'.join(new_lines)

    # 제목에서 4명 → 7명으로 변경
    updated = updated.replace('질문별 응답 정리 (4명)', '질문별 응답 정리 (7명)')

    # 저장 파일명을 7명으로 변경
    out_path = BASE_PATH + "가이드라인_2차_질문별_응답정리_7명.md"
    with open(out_path, 'w', encoding='utf-8') as f:
        f.write(updated)
    print(f"✅ MD 저장 완료: {out_path}")
    return out_path


