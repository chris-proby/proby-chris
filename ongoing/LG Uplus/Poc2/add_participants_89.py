def update_md():
    md_path = BASE_PATH + "가이드라인_2차_질문별_응답정리_7명.md"
    with open(md_path, "r", encoding="utf-8") as f:
        content = f.read()

    lines = content.split("\n")
    new_lines = []

    for line in lines:
        if "`7 - 알 수 없는 그룹.txt` (조수정)" in line and "응답 원본" in line:
            line = line.replace(
                "`7 - 알 수 없는 그룹.txt` (조수정)",
                "`7 - 알 수 없는 그룹.txt` (조수정), `8 - 알 수 없는 그룹.txt` (엄지), `9 - 알 수 없는 그룹.txt` (김건우)",
            )
        elif "참여자 G (조수정)" in line and "| ID |" in line:
            line = line.rstrip()
            if not line.endswith("|"):
                line += " |"
            line = line[:-1] + "| 참여자 H (엄지) | 참여자 I (김건우) |"
        elif (
            re.match(r"^\|----", line)
            and "참여자" not in line
            and "항목" not in line
            and "표기" not in line
            and "ID" not in line
            and "------" in line
        ):
            # 테이블 구분선: 데이터 테이블만 확장 (범례 등 짧은 구분선 제외)
            if line.count("|") >= 8:
                line = line.rstrip()
                if not line.endswith("|"):
                    line += "|"
                line = line + "-------------------|-------------------|"
        elif re.match(r"^\| \*\*Q", line):
            q_match = re.search(r"\*\*(Q[\d-]+)\*\*", line)
            if q_match:
                q_id = q_match.group(1)
                if q_id in NEW_HI:
                    h_cell = NEW_HI[q_id].get("H", NA)
                    i_cell = NEW_HI[q_id].get("I", NA)
                    line = line.rstrip()
                    if line.endswith("|"):
                        line = line[:-1].rstrip()
                    line += f" | {h_cell} | {i_cell} |"

        new_lines.append(line)

    updated = "\n".join(new_lines)
    updated = updated.replace("질문별 응답 정리 (7명)", "질문별 응답 정리 (9명)")

    out_path = BASE_PATH + "가이드라인_2차_질문별_응답정리_9명.md"
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(updated)
    print(f"✅ MD 저장 완료: {out_path}")
    return out_path


def extract_md_table_data(md_path):
    """MD 파일에서 Q 행만 파싱 (참여자 A~G)."""
    with open(md_path, "r", encoding="utf-8") as f:
        content = f.read()

    rows = {}
    for line in content.split("\n"):
        m = re.match(r"^\| \*\*(Q[\d-]+)\*\* \|(.+)", line)
        if m:
            q_id = m.group(1)
            rest = m.group(2)
            parts = rest.split(" | ")
            cells = [p.strip() for p in parts]
            if len(cells) >= 5:
                rows[q_id] = {
                    "질문": cells[0],
                    "전수현": cells[1],
                    "이연진": cells[2],
                    "황수경": cells[3],
                    "김영균": cells[4].rstrip("|").strip(),
                }
    return rows


def clean_cell(text):
    text = text.replace("<br/>", "\n").replace("<br />", "\n")
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
    text = text.replace("「", '"').replace("」", '"')
    text = text.strip().strip("|").strip()
    return text


